# INPUT

# asks for two floats, a and b

a = float(input("Enter a value for float a: "))
b = float(input("Enter a value for float b: "))

# CALCULATE
print("\n\nOUTPUT:\n")
print(f"The sum of a and b is {a + b}\n") # sum
print(f"The difference of a and b is {a - b}") # difference a and b
print(f"The difference of b and a is {b - a}\n") # difference b and a
print(f"The product of a and b is {a * b}\n") # product
print(f"The quotient of a and b is {a / b}") # quotient a and b
print(f"The quotient of b and a is {b / a}") # quotient b and a